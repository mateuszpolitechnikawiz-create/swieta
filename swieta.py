import streamlit as st
import matplotlib.pyplot as plt
import matplotlib.patches as patches
import numpy as np

# Tytu aplikacji Streamlit
st.title(" wity Mikoaj w Streamlit (Matplotlib)")
st.caption("Wygenerowany za pomoc bibliotek Matplotlib i Streamlit.")

def draw_santa():
    """Funkcja rysujca figur witego Mikoaja za pomoc Matplotlib."""

    # Ustawienia figury
    fig, ax = plt.subplots(figsize=(8, 10))
    ax.set_xlim(-10, 10)
    ax.set_ylim(-10, 15)
    ax.set_aspect('equal')
    ax.axis('off') # Usu osie

    # --- PARAMETRY ---
    X_BODY = 0
    Y_BODY = -10
    BODY_WIDTH = 6
    BODY_HEIGHT = 10
    RED = '#D62828'
    WHITE = '#F0F0F0'
    SKIN = '#FADADD'
    BLACK = '#1E1E1E'
    GOLD = '#FFC300'

    # --- 1. KORPUS I NOGI ---
    # Korpus (Czerwony Paszcz)
    body = patches.Rectangle((X_BODY - BODY_WIDTH/2, Y_BODY), BODY_WIDTH, BODY_HEIGHT, color=RED, ec=BLACK, lw=1.5)
    ax.add_patch(body)

    # Biae obszycie na dole paszcza
    body_fur_bottom = patches.Rectangle((X_BODY - BODY_WIDTH/2, Y_BODY), BODY_WIDTH, 1, color=WHITE, ec=BLACK, lw=1.5)
    ax.add_patch(body_fur_bottom)

    # Pasek (czarny)
    belt = patches.Rectangle((X_BODY - BODY_WIDTH/2, -1.5), BODY_WIDTH, 1.5, color=BLACK, ec=BLACK, lw=1.5)
    ax.add_patch(belt)

    # Klamra (偶贸ty)
    buckle = patches.Rectangle((X_BODY - 1.5, -2), 3, 2.5, color=GOLD, ec=BLACK, lw=1.5)
    ax.add_patch(buckle)

    # Nogi (Czerwone Spodnie)
    leg_left = patches.Rectangle((X_BODY - 2.5, -10), 2, 2, color=RED, ec=BLACK, lw=1.5)
    leg_right = patches.Rectangle((X_BODY + 0.5, -10), 2, 2, color=RED, ec=BLACK, lw=1.5)
    ax.add_patch(leg_left)
    ax.add_patch(leg_right)

    # Buty (czarne)
    boot_left = patches.Rectangle((X_BODY - 3, -12), 3, 2, color=BLACK, ec=BLACK, lw=1.5)
    boot_right = patches.Rectangle((X_BODY + 0, -12), 3, 2, color=BLACK, ec=BLACK, lw=1.5)
    ax.add_patch(boot_left)
    ax.add_patch(boot_right)

    # --- 2. GOWA, BRODA I CZAPKA ---
    Y_HEAD = 5
    # Twarz (jasny be偶)
    face = patches.Circle((X_BODY, Y_HEAD), 2.5, color=SKIN, ec=BLACK, lw=1.5)
    ax.add_patch(face)

    # Broda (u偶ycie okrg贸w i uku, aby uzyska ksztat)
    beard_base = patches.Circle((X_BODY, Y_HEAD - 2.5), 3.5, color=WHITE, ec=BLACK, lw=1.5)
    ax.add_patch(beard_base)
    beard_cutout = patches.Rectangle((X_BODY - 2.5, Y_HEAD - 0.5), 5, 3, color=SKIN, ec='none') # Wycicie, by odsoni twarz
    ax.add_patch(beard_cutout)

    # Wsy
    mustache_left = patches.Circle((X_BODY - 1.5, Y_HEAD - 1), 1.5, color=WHITE, ec=BLACK, lw=1.5)
    mustache_right = patches.Circle((X_BODY + 1.5, Y_HEAD - 1), 1.5, color=WHITE, ec=BLACK, lw=1.5)
    ax.add_patch(mustache_left)
    ax.add_patch(mustache_right)

    # Nos (czerwony)
    nose = patches.Circle((X_BODY, Y_HEAD - 0.5), 0.5, color=RED, ec=BLACK, lw=1.5)
    ax.add_patch(nose)

    # Oczy (czarne)
    ax.plot([X_BODY - 1, X_BODY + 1], [Y_HEAD + 0.5, Y_HEAD + 0.5], 'o', markersize=6, color=BLACK)


    # Czapka (Czerwony Tr贸jkt)
    hat_red = patches.Polygon([(-4.5, Y_HEAD + 0.5), (4.5, Y_HEAD + 0.5), (0, Y_HEAD + 8)], closed=True, color=RED, ec=BLACK, lw=1.5)
    ax.add_patch(hat_red)

    # Biae futerko na dole czapki
    hat_fur = patches.Rectangle((-4.5, Y_HEAD), 9, 1, color=WHITE, ec=BLACK, lw=1.5)
    ax.add_patch(hat_fur)

    # Pompon (biay)
    pompon = patches.Circle((0, Y_HEAD + 8), 1.5, color=WHITE, ec=BLACK, lw=1.5)
    ax.add_patch(pompon)


    # --- 3. RCE ---
    # Rka Lewa
    arm_left = patches.Rectangle((X_BODY - 5, 2), 2, 4, angle=45, color=RED, ec=BLACK, lw=1.5)
    ax.add_patch(arm_left)
    # Rkawiczka Lewa
    glove_left = patches.Circle((X_BODY - 5, 4), 1, color=WHITE, ec=BLACK, lw=1.5)
    ax.add_patch(glove_left)

    # Rka Prawa
    arm_right = patches.Rectangle((X_BODY + 3, 2), 2, 4, angle=-45, color=RED, ec=BLACK, lw=1.5)
    ax.add_patch(arm_right)
    # Rkawiczka Prawa
    glove_right = patches.Circle((X_BODY + 5, 4), 1, color=WHITE, ec=BLACK, lw=1.5)
    ax.add_patch(glove_right)


    # --- 4. WOREK Z PREZENTAMI ---
    # Worek (na lewo od Mikoaja)
    bag = patches.Wedge((-7, -5), 4, 270, 90, color='brown', ec=BLACK, lw=1.5, width=4)
    ax.add_patch(bag)
    bag_top = patches.Rectangle((-10, -5), 6, 2, color='brown', ec=BLACK, lw=1.5)
    ax.add_patch(bag_top)
    # Szczyt worka (zawizanie)
    bag_tie = patches.Rectangle((-10, -3), 6, 0.5, color=RED, ec=BLACK, lw=1.5)
    ax.add_patch(bag_tie)

    # Prezenty (wystajce z worka)
    # Niebieski
    gift_blue = patches.Rectangle((-9, -2.5), 1.5, 2, color='blue', ec=BLACK, lw=1)
    ax.add_patch(gift_blue)
    # Zielony
    gift_green = patches.Rectangle((-7, -1.5), 1, 1.5, color='green', ec=BLACK, lw=1)
    ax.add_patch(gift_green)
    # 呕贸ty
    gift_yellow = patches.Rectangle((-8.5, -0.5), 1.5, 0.5, color='yellow', ec=BLACK, lw=1)
    ax.add_patch(gift_yellow)

    # Zwr贸cenie figury do Streamlit
    return fig

# Rysowanie Mikoaja za pomoc Streamlit
st.pyplot(draw_santa())